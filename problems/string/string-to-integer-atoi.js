/**
 * 字符串转换整数 (atoi)
 * 难度：中等
 * 
 * 题目：请你来实现一个 myAtoi(string s) 函数，使其能将字符串转换成一个 32 位有符号整数（类似 C/C++ 中的 atoi 函数）。
 * 函数 myAtoi(string s) 的算法如下：
 * 1. 读入字符串并丢弃无用的前导空格
 * 2. 检查下一个字符（假设还未到字符末尾）为正还是负号，读取该字符（如果有）。 确定最终结果的符号。 如果两者都不存在，则假定结果为正。
 * 3. 读入下一个字符，直到到达下一个非数字字符或到达输入的结尾。字符串的其余部分将被忽略。
 * 4. 将前面步骤读入的这些数字转换为整数（即，"123" -> 123，"-123" -> -123）。如果没有读入数字，则整数为 0 。必要时更改符号（从步骤 2 开始）。
 * 5. 如果整数数超过 32 位有符号整数范围 [−2^31,  2^31 − 1] ，需要截断这个整数，使其保持在这个范围内。具体来说，小于 −2^31 的整数应该被固定为 −2^31 ，大于 2^31 − 1 的整数应该被固定为 2^31 − 1 。
 * 6. 返回整数作为最终结果。
 * 注意：
 * 本题中的空白字符只包括空格字符 ' ' 。
 * 除前导空格或数字后的其余字符串外，请勿忽略 任何其他字符。
 * 
 * 示例：
 * 输入：s = "42"
 * 输出：42
 * 
 * 输入：s = "   -42"
 * 输出：-42
 * 解释：第一个非空白字符为 '-', 它是一个负号。我们尽可能将负号与后面所有连续出现的数字组合起来，最后得到 -42 。
 * 
 * 输入：s = "4193 with words"
 * 输出：4193
 * 解释：转换截止于数字 '3' ，因为它的下一个字符不是数字。
 * 
 * 输入：s = "words and 987"
 * 输出：0
 * 解释：第一个非空字符是 'w', 但它不是数字或正、负号。因此没有有效数字可以转换，所以输出 0 。
 * 
 * 输入：s = "-91283472332"
 * 输出：-2147483648
 * 解释：数字 "-91283472332" 超过 32 位有符号整数范围。因此返回 INT_MIN (−2^31) 。
 * 
 * 解题思路：
 * 1. 首先跳过前导空格
 * 2. 处理符号位
 * 3. 读取数字，直到遇到非数字字符
 * 4. 检查是否溢出
 * 5. 返回结果
 * 
 * 时间复杂度：O(n)，其中 n 是字符串的长度。每个字符会被访问一次。
 * 空间复杂度：O(1)，只使用了常数级别的额外空间。
 */

/**
 * @param {string} s
 * @return {number}
 */
var myAtoi = function(s) {
    const INT_MAX = 2147483647;
    const INT_MIN = -2147483648;
    
    let i = 0;
    let sign = 1;
    let result = 0;
    
    // 跳过空格
    while (i < s.length && s[i] === ' ') {
        i++;
    }
    
    // 处理符号
    if (i < s.length && (s[i] === '+' || s[i] === '-')) {
        sign = s[i] === '+' ? 1 : -1;
        i++;
    }
    
    // 处理数字
    while (i < s.length && s[i] >= '0' && s[i] <= '9') {
        const digit = parseInt(s[i]);
        
        // 检查是否溢出
        if (result > Math.floor((INT_MAX - digit) / 10)) {
            return sign === 1 ? INT_MAX : INT_MIN;
        }
        
        result = result * 10 + digit;
        i++;
    }
    
    return result * sign;
};

// 测试用例
console.log(myAtoi("42")); // 输出: 42
console.log(myAtoi("   -42")); // 输出: -42
console.log(myAtoi("4193 with words")); // 输出: 4193
console.log(myAtoi("words and 987")); // 输出: 0
console.log(myAtoi("-91283472332")); // 输出: -2147483648