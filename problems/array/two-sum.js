/**
 * 两数之和
 * 题目：给定一个整数数组 nums 和一个整数目标值 target，请你在该数组中找出和为目标值 target 的那两个整数，并返回它们的数组下标。
 * 你可以假设每种输入只会对应一个答案。但是，数组中同一个元素在答案里不能重复出现。
 * 你可以按任意顺序返回答案。
 * 
 * 示例：
 * 输入：nums = [2,7,11,15], target = 9
 * 输出：[0,1]
 * 解释：因为 nums[0] + nums[1] == 9 ，返回 [0, 1]
 * 
 * 解题思路：
 * 1. 使用哈希表（对象）来存储已经遍历过的元素及其索引
 * 2. 遍历数组，对于每个元素，计算目标值与当前元素的差值
 * 3. 检查哈希表中是否存在该差值，如果存在，返回当前元素的索引和哈希表中差值的索引
 * 4. 如果不存在，将当前元素及其索引存入哈希表
 * 
 * 时间复杂度：O(n)，其中 n 是数组的长度。我们只需要遍历数组一次，哈希表的查找时间是 O(1)。
 * 空间复杂度：O(n)，其中 n 是数组的长度。哈希表最多需要存储 n 个键值对。
 */

/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number[]}
 */
var twoSum = function(nums, target) {
    // 创建一个哈希表来存储已经遍历过的元素及其索引
    const map = new Map();
    
    // 遍历数组
    for (let i = 0; i < nums.length; i++) {
        // 计算目标值与当前元素的差值
        const complement = target - nums[i];
        
        // 检查哈希表中是否存在该差值
        if (map.has(complement)) {
            // 如果存在，返回当前元素的索引和哈希表中差值的索引
            return [map.get(complement), i];
        }
        
        // 如果不存在，将当前元素及其索引存入哈希表
        map.set(nums[i], i);
    }
    
    // 如果没有找到，返回空数组
    return [];
};

// 测试用例
console.log(twoSum([2,7,11,15], 9)); // 输出：[0, 1]
console.log(twoSum([3,2,4], 6)); // 输出：[1, 2]
console.log(twoSum([3,3], 6)); // 输出：[0, 1]