/**
 * 打家劫舍 II
 * 难度：中等
 * 
 * 题目：你是一个专业的小偷，计划偷窃沿街的房屋，每间房内都藏有一定的现金。这个地方所有的房屋都 围成一圈 ，这意味着第一个房屋和最后一个房屋是紧挨着的。同时，相邻的房屋装有相互连通的防盗系统，如果两间相邻的房屋在同一晚上被小偷闯入，系统会自动报警 。
 * 给定一个代表每个房屋存放金额的非负整数数组，计算你 在不触动警报装置的情况下 ，今晚能够偷窃到的最高金额。
 * 
 * 示例：
 * 输入：nums = [2, 3, 2]
 * 输出：3
 * 解释：你不能先偷窃 1 号房屋（金额 = 2），然后偷窃 3 号房屋（金额 = 2）, 因为他们是相邻的。
 * 
 * 输入：nums = [1, 2, 3, 1]
 * 输出：4
 * 解释：你可以先偷窃 1 号房屋（金额 = 1），然后偷窃 3 号房屋（金额 = 3）。偷窃到的最高金额 = 1 + 3 = 4 。
 * 
 * 输入：nums = [0]
 * 输出：0
 * 
 * 解题思路：
 * 1. 这是打家劫舍问题的变种，由于房屋围成一圈，所以第一个房屋和最后一个房屋不能同时被偷
 * 2. 我们可以将问题分解为两个子问题：
 *    a. 不偷第一个房屋，只考虑从第二个房屋到最后一个房屋
 *    b. 不偷最后一个房屋，只考虑从第一个房屋到倒数第二个房屋
 * 3. 对每个子问题，使用动态规划的方法求解
 * 4. 最后取两个子问题的最大值
 * 
 * 时间复杂度：O(n)，其中 n 是房屋的数量。每个房屋会被访问一次。
 * 空间复杂度：O(1)，只使用了常数级别的额外空间。
 */

/**
 * @param {number[]} nums
 * @return {number}
 */
var rob = function(nums) {
    if (nums.length === 0) return 0;
    if (nums.length === 1) return nums[0];
    
    // 情况1：不偷第一个房子
    function robRange(start, end) {
        let prev1 = 0;
        let prev2 = 0;
        
        for (let i = start; i <= end; i++) {
            const current = Math.max(prev1, prev2 + nums[i]);
            prev2 = prev1;
            prev1 = current;
        }
        
        return prev1;
    }
    
    // 情况1：不偷第一个房子，情况2：不偷最后一个房子
    return Math.max(robRange(1, nums.length - 1), robRange(0, nums.length - 2));
};

// 测试用例
console.log(rob([2, 3, 2])); // 输出: 3
console.log(rob([1, 2, 3, 1])); // 输出: 4
console.log(rob([0])); // 输出: 0