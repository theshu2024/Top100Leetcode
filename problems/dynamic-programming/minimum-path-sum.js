/**
 * 最小路径和
 * 难度：中等
 * 
 * 题目：给定一个包含非负整数的 m x n 网格 grid ，请找出一条从左上角到右下角的路径，使得路径上的数字总和为最小。
 * 说明：每次只能向下或者向右移动一步。
 * 
 * 示例：
 * 输入：grid = [[1,3,1],[1,5,1],[4,2,1]]
 * 输出：7
 * 解释：因为路径 1→3→1→1→1 的总和最小。
 * 
 * 输入：grid = [[1,2,3],[4,5,6]]
 * 输出：12
 * 
 * 解题思路：
 * 1. 使用动态规划的方法
 * 2. 创建一个二维dp数组，其中 dp[i][j] 表示到达 (i,j) 的最小路径和
 * 3. 初始化起点：dp[0][0] = grid[0][0]
 * 4. 初始化第一行：每个位置的最小路径和等于前一个位置的最小路径和加上当前位置的值
 * 5. 初始化第一列：每个位置的最小路径和等于上一个位置的最小路径和加上当前位置的值
 * 6. 填充dp数组：对于每个位置 (i,j)，最小路径和等于上方和左方位置的最小路径和中的较小值加上当前位置的值
 * 7. 返回 dp[m-1][n-1]，即到达右下角的最小路径和
 * 
 * 时间复杂度：O(m*n)，其中 m 和 n 是网格的行数和列数。每个位置会被访问一次。
 * 空间复杂度：O(m*n)，其中 m 和 n 是网格的行数和列数。需要额外的二维数组来存储 dp 值。
 */

/**
 * @param {number[][]} grid
 * @return {number}
 */
var minPathSum = function(grid) {
    const m = grid.length;
    const n = grid[0].length;
    
    // 创建dp数组，dp[i][j]表示到达(i,j)的最小路径和
    const dp = new Array(m).fill(0).map(() => new Array(n).fill(0));
    
    // 初始化起点
    dp[0][0] = grid[0][0];
    
    // 初始化第一行
    for (let j = 1; j < n; j++) {
        dp[0][j] = dp[0][j-1] + grid[0][j];
    }
    
    // 初始化第一列
    for (let i = 1; i < m; i++) {
        dp[i][0] = dp[i-1][0] + grid[i][0];
    }
    
    // 填充dp数组
    for (let i = 1; i < m; i++) {
        for (let j = 1; j < n; j++) {
            dp[i][j] = Math.min(dp[i-1][j], dp[i][j-1]) + grid[i][j];
        }
    }
    
    return dp[m-1][n-1];
};

// 测试用例
console.log(minPathSum([[1,3,1],[1,5,1],[4,2,1]])); // 输出: 7
console.log(minPathSum([[1,2,3],[4,5,6]])); // 输出: 12