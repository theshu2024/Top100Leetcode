/**
 * 打家劫舍
 * 难度：中等
 * 
 * 题目：你是一个专业的小偷，计划偷窃沿街的房屋。每间房内都藏有一定的现金，影响你偷窃的唯一制约因素就是相邻的房屋装有相互连通的防盗系统，如果两间相邻的房屋在同一晚上被小偷闯入，系统会自动报警。
 * 给定一个代表每个房屋存放金额的非负整数数组，计算你 不触动警报装置的情况下 ，一夜之内能够偷窃到的最高金额。
 * 
 * 示例：
 * 输入：[1, 2, 3, 1]
 * 输出：4
 * 解释：偷窃 1 号房屋 (金额 = 1) ，然后偷窃 3 号房屋 (金额 = 3)。
 * 偷窃到的最高金额 = 1 + 3 = 4 。
 * 
 * 输入：[2, 7, 9, 3, 1]
 * 输出：12
 * 解释：偷窃 1 号房屋 (金额 = 2), 偷窃 3 号房屋 (金额 = 9)，接着偷窃 5 号房屋 (金额 = 1)。
 * 偷窃到的最高金额 = 2 + 9 + 1 = 12 。
 * 
 * 输入：[0]
 * 输出：0
 * 
 * 解题思路：
 * 1. 使用动态规划来解决这个问题
 * 2. 定义 dp[i] 表示前 i 个房屋能偷到的最大金额
 * 3. 状态转移方程：对于第 i 个房屋，有两种选择：
 *    a. 偷第 i 个房屋，那么就不能偷第 i-1 个房屋，所以金额是 dp[i-2] + nums[i]
 *    b. 不偷第 i 个房屋，那么金额就是 dp[i-1]
 * 4. 初始状态：dp[0] = nums[0]，dp[1] = max(nums[0], nums[1])
 * 5. 最终结果是 dp[nums.length - 1]
 * 
 * 时间复杂度：O(n)，其中 n 是房屋的数量。每个房屋会被访问一次。
 * 空间复杂度：O(n)，需要存储 dp 数组。
 */

/**
 * @param {number[]} nums
 * @return {number}
 */
var rob = function(nums) {
    if (nums.length === 0) return 0;
    if (nums.length === 1) return nums[0];
    
    // dp[i] 表示前i个房子能偷到的最大金额
    const dp = new Array(nums.length);
    dp[0] = nums[0];
    dp[1] = Math.max(nums[0], nums[1]);
    
    for (let i = 2; i < nums.length; i++) {
        // 对于第i个房子，有两种选择：偷或不偷
        // 偷的话，就不能偷第i-1个房子，所以金额是dp[i-2] + nums[i]
        // 不偷的话，金额就是dp[i-1]
        dp[i] = Math.max(dp[i-1], dp[i-2] + nums[i]);
    }
    
    return dp[nums.length - 1];
};

// 测试用例
console.log(rob([1, 2, 3, 1])); // 输出: 4
console.log(rob([2, 7, 9, 3, 1])); // 输出: 12
console.log(rob([0])); // 输出: 0