/**
 * 不同路径
 * 难度：中等
 * 
 * 题目：一个机器人位于一个 m x n 网格的左上角 （起始点在下图中标记为 “Start” ）。
 * 机器人每次只能向下或者向右移动一步。机器人试图达到网格的右下角（在下图中标记为 “Finish”）。
 * 问总共有多少条不同的路径？
 * 
 * 示例：
 * 输入：m = 3, n = 7
 * 输出：28
 * 
 * 输入：m = 3, n = 2
 * 输出：3
 * 解释：
 * 从左上角开始，总共有 3 条路径可以到达右下角。
 * 1. 向右 -> 向下 -> 向下
 * 2. 向下 -> 向下 -> 向右
 * 3. 向下 -> 向右 -> 向下
 * 
 * 解题思路：
 * 1. 使用动态规划的方法
 * 2. 创建一个二维dp数组，其中 dp[i][j] 表示到达 (i,j) 的不同路径数
 * 3. 初始化第一行和第一列：因为机器人只能向右或向下移动，所以第一行和第一列的每个位置只有一种路径
 * 4. 填充dp数组：对于每个位置 (i,j)，到达该位置的路径数等于上方和左方位置的路径数之和
 * 5. 返回 dp[m-1][n-1]，即到达右下角的不同路径数
 * 
 * 时间复杂度：O(m*n)，其中 m 和 n 是网格的行数和列数。每个位置会被访问一次。
 * 空间复杂度：O(m*n)，其中 m 和 n 是网格的行数和列数。需要额外的二维数组来存储 dp 值。
 */

/**
 * @param {number} m
 * @param {number} n
 * @return {number}
 */
var uniquePaths = function(m, n) {
    // 创建dp数组，dp[i][j]表示到达(i,j)的路径数
    const dp = new Array(m).fill(0).map(() => new Array(n).fill(0));
    
    // 初始化第一行和第一列
    for (let i = 0; i < m; i++) {
        dp[i][0] = 1;
    }
    for (let j = 0; j < n; j++) {
        dp[0][j] = 1;
    }
    
    // 填充dp数组
    for (let i = 1; i < m; i++) {
        for (let j = 1; j < n; j++) {
            dp[i][j] = dp[i-1][j] + dp[i][j-1];
        }
    }
    
    return dp[m-1][n-1];
};

// 测试用例
console.log(uniquePaths(3, 7)); // 输出: 28
console.log(uniquePaths(3, 2)); // 输出: 3