/**
 * 位1的个数
 * 难度：简单
 * 
 * 题目：编写一个函数，输入是一个无符号整数（以二进制串的形式），返回其二进制表达式中数字位数为 '1' 的个数（也被称为汉明重量）。
 * 
 * 示例：
 * 输入：00000000000000000000000000001011
 * 输出：3
 * 解释：输入的二进制串 00000000000000000000000000001011 中，共有三位为 '1'。
 * 
 * 输入：00000000000000000000000010000000
 * 输出：1
 * 解释：输入的二进制串 00000000000000000000000010000000 中，共有一位为 '1'。
 * 
 * 输入：11111111111111111111111111111101
 * 输出：31
 * 解释：输入的二进制串 11111111111111111111111111111101 中，共有 31 位为 '1'。
 * 
 * 解题思路：
 * 1. 使用位运算，每次将 n 与 1 进行与运算，判断最后一位是否为 1
 * 2. 如果是 1，计数器加 1
 * 3. 将 n 右移一位，继续判断
 * 4. 重复步骤 1-3，直到 n 为 0
 * 
 * 时间复杂度：O(1)，因为无符号整数的位数是固定的（32位）。
 * 空间复杂度：O(1)，只使用了常数级别的额外空间。
 */

/**
 * @param {number} n - a positive integer
 * @return {number}
 */
var hammingWeight = function(n) {
    let count = 0;
    
    // 当 n 不为 0 时，继续循环
    while (n) {
        // 判断最后一位是否为 1
        if (n & 1) {
            count++;
        }
        // 将 n 右移一位
        n >>>= 1;
    }
    
    return count;
};

// 测试用例
console.log(hammingWeight(0b00000000000000000000000000001011)); // 输出：3
console.log(hammingWeight(0b00000000000000000000000010000000)); // 输出：1
console.log(hammingWeight(0b11111111111111111111111111111101)); // 输出：31