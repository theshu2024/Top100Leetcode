/**
 * 颠倒二进制位
 * 难度：简单
 * 
 * 题目：颠倒给定的 32 位无符号整数的二进制位。
 * 提示：
 * 请注意，在某些语言（如 Java）中，没有无符号整数类型。在这种情况下，输入和输出都将被指定为有符号整数类型，并且不应影响您的实现，因为无论整数是有符号的还是无符号的，其内部的二进制表示形式都是相同的。
 * 在 Java 中，编译器使用二进制补码记法来表示有符号整数。因此，在 示例 2 中，输入表示有符号整数 -3，输出表示有符号整数 -1073741825。
 * 
 * 示例：
 * 输入：n = 00000010100101000001111010011100
 * 输出：964176192 (00111001011110000010100101000000)
 * 解释：输入的二进制串 00000010100101000001111010011100 表示无符号整数 43261596，因此返回 964176192，其二进制表示形式为 00111001011110000010100101000000。
 * 
 * 输入：n = 11111111111111111111111111111101
 * 输出：3221225471 (10111111111111111111111111111111)
 * 解释：输入的二进制串 11111111111111111111111111111101 表示无符号整数 4294967293，因此返回 3221225471，其二进制表示形式为 10111111111111111111111111111111。
 * 
 * 解题思路：
 * 1. 初始化结果为 0
 * 2. 遍历 32 位，对于每一位：
 *    a. 取出输入整数的第 i 位
 *    b. 将该位放到结果的对应位置（31 - i）
 * 3. 最后将结果转换为无符号整数并返回
 * 
 * 时间复杂度：O(1)，因为只需要遍历 32 位。
 * 空间复杂度：O(1)，只使用了常数级别的额外空间。
 */

/**
 * @param {number} n - a positive integer
 * @return {number} - a positive integer
 */
var reverseBits = function(n) {
    let result = 0;
    
    for (let i = 0; i < 32; i++) {
        // 取出n的第i位
        const bit = (n >> i) & 1;
        // 将bit放到result的对应位置
        result |= (bit << (31 - i));
    }
    
    // 转换为无符号整数
    return result >>> 0;
};

// 测试用例
console.log(reverseBits(43261596)); // 输出: 964176192
console.log(reverseBits(4294967293)); // 输出: 3221225471