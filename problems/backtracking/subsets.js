/**
 * 子集
 * 题目：给你一个整数数组 nums ，数组中的元素 互不相同 。返回该数组所有可能的子集（幂集）。
 * 解集 不能 包含重复的子集。你可以按 任意顺序 返回解集。
 * 
 * 示例：
 * 输入：nums = [1,2,3]
 * 输出：[[],[1],[2],[1,2],[3],[1,3],[2,3],[1,2,3]]
 * 
 * 解题思路：
 * 1. 使用回溯法，遍历所有可能的子集
 * 2. 维护一个路径数组，记录当前已经选择的元素
 * 3. 维护一个起始索引，避免重复选择元素
 * 4. 对于每个元素，有两种选择：选择或不选择
 * 5. 当遍历完所有元素时，将路径数组加入结果集
 * 
 * 时间复杂度：O(2^n)，其中 n 是数组的长度。每个元素有两种选择，总共有 2^n 种可能的子集。
 * 空间复杂度：O(n)，递归的深度最多为 n，需要额外的空间来存储路径。
 */

/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var subsets = function(nums) {
    const result = [];
    // 路径数组，记录当前已经选择的元素
    const path = [];
    
    // 回溯函数
    function backtrack(start) {
        // 将当前路径加入结果集
        result.push([...path]);
        
        // 遍历数组，从起始索引开始
        for (let i = start; i < nums.length; i++) {
            // 选择当前元素
            path.push(nums[i]);
            // 递归调用，起始索引为 i + 1，避免重复选择元素
            backtrack(i + 1);
            // 回溯，移除当前元素
            path.pop();
        }
    }
    
    // 调用回溯函数，起始索引为 0
    backtrack(0);
    
    return result;
};

// 测试用例
console.log(subsets([1,2,3])); // 输出：[[],[1],[2],[1,2],[3],[1,3],[2,3],[1,2,3]]
console.log(subsets([0])); // 输出：[[],[0]]