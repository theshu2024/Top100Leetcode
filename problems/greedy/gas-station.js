/**
 * 加油站
 * 难度：中等
 * 
 * 题目：在一条环路上有 n 个加油站，其中第 i 个加油站有汽油 gas[i] 升。
 * 你有一辆油箱容量无限的汽车，从第 i 个加油站开往第 i+1 个加油站需要消耗汽油 cost[i] 升。你从其中一个加油站出发，开始时油箱为空。
 * 给定两个整数数组 gas 和 cost ，如果你可以绕环路行驶一周，则返回出发时加油站的编号，否则返回 -1 。
 * 如果存在解，则 保证 它是 唯一 的。
 * 
 * 示例：
 * 输入：gas = [1, 2, 3, 4, 5], cost = [3, 4, 5, 1, 2]
 * 输出：3
 * 解释：
 * 从 3 号加油站(索引为 3 处)出发，可获得 4 升汽油。此时油箱有 = 0 + 4 = 4 升汽油
 * 开往 4 号加油站，此时油箱有 4 - 1 + 5 = 8 升汽油
 * 开往 0 号加油站，此时油箱有 8 - 2 + 1 = 7 升汽油
 * 开往 1 号加油站，此时油箱有 7 - 3 + 2 = 6 升汽油
 * 开往 2 号加油站，此时油箱有 6 - 4 + 3 = 5 升汽油
 * 开往 3 号加油站，你需要消耗 5 升汽油，正好足够你返回到 3 号加油站。
 * 因此，3 可为起始索引。
 * 
 * 输入：gas = [2, 3, 4], cost = [3, 4, 3]
 * 输出：-1
 * 解释：
 * 你不能从 0 号或 1 号加油站出发，因为没有足够的汽油可以让你行驶到下一个加油站。
 * 从 2 号加油站出发，你可以获得 4 升汽油，此时油箱有 = 0 + 4 = 4 升汽油
 * 开往 0 号加油站，此时油箱有 4 - 3 + 2 = 3 升汽油
 * 开往 1 号加油站，此时油箱有 3 - 3 + 3 = 3 升汽油
 * 你无法返回 2 号加油站，因为返程需要消耗 4 升汽油，但是你的油箱只有 3 升汽油。
 * 因此，无论怎样，你都不可能绕环路行驶一周。
 * 
 * 解题思路：
 * 1. 使用贪心算法来解决这个问题
 * 2. 遍历所有加油站，计算总油量和总消耗
 * 3. 同时计算当前油量，如果当前油量为负，说明从当前起点到当前位置的路径不可行，将起点设置为下一个加油站，并重置当前油量
 * 4. 最后，如果总油量大于等于总消耗，返回起点，否则返回 -1
 * 
 * 时间复杂度：O(n)，其中 n 是加油站的数量。每个加油站会被访问一次。
 * 空间复杂度：O(1)，只使用了常数级别的额外空间。
 */

/**
 * @param {number[]} gas
 * @param {number[]} cost
 * @return {number}
 */
var canCompleteCircuit = function(gas, cost) {
    const n = gas.length;
    let totalGas = 0;
    let totalCost = 0;
    let currentGas = 0;
    let startStation = 0;
    
    for (let i = 0; i < n; i++) {
        totalGas += gas[i];
        totalCost += cost[i];
        currentGas += gas[i] - cost[i];
        
        // 如果当前油量为负，说明从startStation到i的路径不可行
        // 从i+1重新开始计算
        if (currentGas < 0) {
            startStation = i + 1;
            currentGas = 0;
        }
    }
    
    // 如果总油量大于等于总消耗，说明存在解
    return totalGas >= totalCost ? startStation : -1;
};

// 测试用例
console.log(canCompleteCircuit([1, 2, 3, 4, 5], [3, 4, 5, 1, 2])); // 输出: 3
console.log(canCompleteCircuit([2, 3, 4], [3, 4, 3])); // 输出: -1